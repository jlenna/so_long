#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	char	*i;

	i = 0;
	if (c > 127)
		c = c % 128;
	while (*str != '\0')
	{
		if (*str == c)
			i = (char *)str;
		str++;
	}
	if ((*str == '\0') && (c == '\0'))
		return ((char *)str);
	return (i);
}
